
package bitwiseop;

public class BitwiseOp {

    public static void main(String[] args) {
        int x=10, y=6, z;
        z = x&y;
        System.out.println(z);
        
        int p=10, q=6, r;
        r = p | q;
        System.out.println(r);
        
        int m=10, n=6,a;
        a = m ^ n;
        System.out.println(a);
        
    }
    
}
