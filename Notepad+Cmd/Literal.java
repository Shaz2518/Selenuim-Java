import java.lang.*;
class Literals
{ 
	public static void main(String args[])
	{
		byte b1_decimal=11;
		byte b2_binary=0b1011;
		byte b3_octal=013;
		byte b4_hexa=0xB;
		System.out.println("Decimal value: " + b1_decimal);
		System.out.println("Binary value: " + b2_binary);
		System.out.println("Octal value: " + b3_octal);
		System.out.println("Hexa value: " + b4_hexa);
	}
}
	 